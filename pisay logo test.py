import turtle

turtle.showturtle()

turtle.fillcolor("black")
turtle.color("black")
turtle.penup()
turtle.goto(-100,0)
turtle.pendown()
turtle.begin_fill()
turtle.circle(100,100)
turtle.setheading(270)
turtle.circle(100,100)
turtle.setheading(180)
turtle.circle(100,100)
turtle.setheading(90)
turtle.circle(100,100)
turtle.end_fill()
